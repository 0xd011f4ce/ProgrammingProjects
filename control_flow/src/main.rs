fn main ()
{
		// if and else-if
		let some_condition: bool = true;
		if some_condition
		{
				println! ("This line will be output!");
		}

		let season: &str = "Summer";

		if season == "Summer"
		{
				println! ("School's out!");
		}
		else if season == "Winter"
		{
				println! ("Brr, so cold!");
		}
		else if season == "Fall"
		{
				println! ("Leaves are falling!");
		}
		else if season == "Spring"
		{
				println! ("Lots of rain!");
		}
}
